function checkForTampering(plugin, paramState, sampleRate, cause)
% Verify parameters were not tampered with
if ~isequal(paramState.pitch_shift, plugin.pitch_shift)
    error('ValidateAudioPlugin:ParamChanged', ...
        '%s changed parameter ''pitch_shift'' from %g to %g.', ...
        cause, paramState.pitch_shift, plugin.pitch_shift);
end
if ~isequal(paramState.autotune, plugin.autotune)
    error('ValidateAudioPlugin:ParamChanged', ...
        '%s changed parameter ''autotune'' from %g to %g.', ...
        cause, paramState.autotune, plugin.autotune);
end
if ~isequal(paramState.key, plugin.key)
    error('ValidateAudioPlugin:ParamChanged', ...
        '%s changed parameter ''key'' from ''%s'' to ''%s'.'', ...
        cause, paramState.key, plugin.key);
end
if ~isequal(paramState.scale, plugin.scale)
    error('ValidateAudioPlugin:ParamChanged', ...
        '%s changed parameter ''scale'' from ''%s'' to ''%s'.'', ...
        cause, paramState.scale, plugin.scale);
end
% Verify sample rate was not tampered with
if ~isequal(getSampleRate(plugin), sampleRate)
    error('ValidateAudioPlugin:SampleRateChanged', ...
        '%s changed sample rate from %g to %g.', ...
        cause, sampleRate, getSampleRate(plugin));
end
end

function y = logchirp(f0, f1, Fs, nsamples, initialPhase)
% logarithmically swept sine from f0 to f1 over nsamples, at Fs
y = zeros(nsamples,numel(initialPhase));
instPhi = logInstantaneousPhase(f0, f1, Fs, nsamples);
for i = 1:numel(initialPhase)
    y(:,i) = sin(instPhi + initialPhase(i));
end
end

function phi = logInstantaneousPhase(f0, f1, Fs, n)
final = n-1;
t = (0:final)/final;
t1 = final/Fs;
phi = 2*pi * t1/log(f1/f0) * (f0 * (f1/f0).^(t') - f0);
end

function paramState = initParamState(plugin)
paramState.pitch_shift = plugin.pitch_shift;
paramState.autotune = plugin.autotune;
paramState.key = plugin.key;
paramState.scale = plugin.scale;
end

function val = fromNormalizedpitch_shift(normval)
val = -24 + (24--24)*normval;
end

function val = fromNormalizedautotune(normval)
val = logical(floor(0.5 + normval));
end

function val = fromNormalizedkey(normval)
idx = floor( 0.5 + normval * 11);
switch idx
    case 0
        val = 'C';
    case 1
        val = 'C#';
    case 2
        val = 'D';
    case 3
        val = 'D#';
    case 4
        val = 'E';
    case 5
        val = 'F';
    case 6
        val = 'F#';
    case 7
        val = 'G';
    case 8
        val = 'G#';
    case 9
        val = 'A';
    case 10
        val = 'A#';
    otherwise
        val = 'B';
end
end

function val = fromNormalizedscale(normval)
idx = floor( 0.5 + normval * 2);
switch idx
    case 0
        val = 'Chromatic';
    case 1
        val = 'Major';
    otherwise
        val = 'Minor';
end
end
